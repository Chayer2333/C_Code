/*
 对数组a的10个元素赋0到9的值，并按反向输出。
*/
//int a=1,b=2,......z=10; 
#include<stdio.h>
int main()
{
	//下标 0开始 范围0到数组长度减1 
	//a[0] a[1] ....a[9]
	int i,a[10];
	for(i=0;i<=9;i++){//i=0 1 2.....9 10
	//a[0]=1 a[1]=2 a[2]=3.....a[9]=10
		scanf("%d",&a[i]);
	}
	for(i=9;i>=0;i--){//i=9 8....0
		printf("%d ",a[i]);//a[9] a[8]..... a[0]
	}
	printf("\n");
	return 0;
}

